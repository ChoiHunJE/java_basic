package a03_형변환;

public class Conversion {

	public static void main(String[] args) {
		char c ='a';
		int i_c = c;
		System.out.println(i_c);
		
		
		int i = 100;
		char c_i = (char)i;
		System.out.println(c_i);
		
		
		
		double d = (double)i;
		System.out.println(d);
		
		d = 3.14;
		int i_d = (int)d;
		System.out.println(i_d);
		
		
		
		
		
		
		
	}

}
